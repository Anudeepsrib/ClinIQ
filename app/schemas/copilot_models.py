"""Pydantic models for the clinical quick-help integration."""

from typing import List, Literal, Optional

from pydantic import BaseModel, Field


class CopilotHelpRequest(BaseModel):
    question: str = Field(..., min_length=1, description="The clinical question to ask")
    context: Optional[str] = None  # optional patient/clinical context
    department: Optional[str] = None
    provider: Optional[Literal["google_gemma", "azure_openai", "ollama", "vllm"]] = Field(
        None,
        description=(
            "Request-level LLM provider override "
            "('google_gemma' | 'azure_openai' | 'ollama' | 'vllm')"
        ),
    )
    images: Optional[List[str]] = Field(
        None, description="List of Base64 encoded images for multimodal analysis (radiology, forms, etc.)"
    )


class CopilotSource(BaseModel):
    title: str
    url: Optional[str] = None
    provider: str = "Google Gemma 4"


class CopilotHelpResponse(BaseModel):
    answer: str
    sources: List[CopilotSource] = []
    confidence: str = "medium"  # "high" | "medium" | "low"
    disclaimer: str = (
        "This information is generated by the configured clinical intelligence model for reference only. "
        "Always verify with institutional protocols and clinical judgement."
    )
    response_type: str = "clinical_intelligence"
